/**
 * Generates and shares the front label as a PDF file.
 */
export async function shareFrontLabel(caseData: Case, qrBase64: string): Promise<void> {
  if (!(await Sharing.isAvailableAsync())) {
    throw new Error('Sharing is not available on this device');
  }

  const htmlContent = getFrontLabelHtml(caseData, qrBase64);
  try {
    // Generate PDF to temporary location
    const { uri } = await Print.printToFileAsync({ html: htmlContent });
    
    // Create a user-friendly name for the PDF
    const cleanFileName = `Front_Label_${caseData.file_number.replace(/[^a-zA-Z0-9-_]/g, '_')}.pdf`;
    const newUri = `${FileSystem.cacheDirectory}${cleanFileName}`;
    
    // Copy/move the file to cached directory with the user-friendly name
    await FileSystem.moveAsync({
      from: uri,
      to: newUri,
    });

    await Sharing.shareAsync(newUri, {
      mimeType: 'application/pdf',
      dialogTitle: `Share Front Label ${caseData.file_number}`,
      UTI: 'com.adobe.pdf',
    });
  } catch (error) {
    console.error('Error sharing front label PDF:', error);
    throw error;
  }
}

