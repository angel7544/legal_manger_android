/**
 * Prints the front label sticker for a physical legal file folder.
 * 
 * @param caseData Case record details
 * @param qrBase64 Base64 image data URI of the QR code
 */
export async function printFrontLabel(caseData: Case, qrBase64: string): Promise<void> {
  const htmlContent = getFrontLabelHtml(caseData, qrBase64);
  try {
    await Print.printAsync({
      html: htmlContent,
    });
  } catch (error) {
    console.error('Error printing front label:', error);
    throw error;
  }
}

